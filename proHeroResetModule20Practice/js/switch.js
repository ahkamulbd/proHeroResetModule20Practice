var color = 'yellow';

switch (color) {
    case 'green':
        console.log('Green Friend');
        break;
    case 'red':
        console.log('Red Friend');
        break;
    case 'yellow':
        console.log('Yellow Friend');
        break;
    case 'orange':
        console.log('Orange Friend');
        break;
    default:
        console.log('Kala Kala Friend');
}